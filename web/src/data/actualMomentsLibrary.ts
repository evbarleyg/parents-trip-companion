import type { TripActualMoment, TripActualPhoto, TripActualVideo } from '../types';

const PHOTO_LIBRARY_SOURCE = 'Family photo library';

interface PhotoSeed {
  id: string;
  src: string;
  originalName: string;
  capturedAt: string;
  label: string;
  alt?: string;
  caption?: string;
  lat?: number;
  lng?: number;
  convertedFromHeic?: boolean;
}

interface VideoSeed {
  id: string;
  src: string;
  caption: string;
  poster?: string;
}

function formatGps(lat: number | undefined, lng: number | undefined): string {
  if (typeof lat !== 'number' || typeof lng !== 'number') return '';
  return ` | GPS ${lat.toFixed(6)}, ${lng.toFixed(6)}`;
}

function buildPhoto(seed: PhotoSeed): TripActualPhoto {
  return {
    id: seed.id,
    src: seed.src,
    alt: seed.alt || `${seed.label} captured during the trip.`,
    caption:
      seed.caption ||
      `Captured ${seed.capturedAt}${seed.convertedFromHeic ? ' | rendered from HEIC for web use' : ''}${formatGps(
        seed.lat,
        seed.lng,
      )}`,
    lat: seed.lat,
    lng: seed.lng,
  };
}

function buildVideo(seed: VideoSeed): TripActualVideo {
  return {
    id: seed.id,
    src: seed.src,
    caption: seed.caption,
    poster: seed.poster,
  };
}

function buildPhotoMoment(
  date: string,
  whenLabel: string,
  text: string,
  seeds: PhotoSeed[],
  videoSeeds: VideoSeed[] = [],
): TripActualMoment {
  return {
    id: `actual-${date}-photo-library`,
    source: PHOTO_LIBRARY_SOURCE,
    whenLabel,
    text,
    photos: [...seeds].sort((left, right) => right.capturedAt.localeCompare(left.capturedAt)).map(buildPhoto),
    videos: videoSeeds.map(buildVideo),
  };
}

